// ─────────────────────────────────────────────────────────────────────────────
// bake-defaults.js — writes electron/baked-defaults.ts from env vars.
//
// Why this exists: the Windows installer has no way to inject an env var into
// a double-click launch, so anything that looks like "production config" has
// to be baked INTO the binary at build time. Devs don't set these — the file
// is committed with localhost defaults, and release CI overrides them via
// env vars:
//
//   VCFO_DEFAULT_SERVER_URL=https://vcfo.magnacode.in npm run dist
//
// Runtime precedence inside main.ts:
//   1. process.env.VCFO_DEFAULT_SERVER_URL (dev override)
//   2. BAKED.serverUrl from this generated file (installer default)
//   3. Hard-coded localhost fallback (if someone deletes the file)
// ─────────────────────────────────────────────────────────────────────────────

const fs = require('node:fs');
const path = require('node:path');

const DEFAULTS = {
  serverUrl: process.env.VCFO_DEFAULT_SERVER_URL || 'http://localhost:3001',
  buildChannel: process.env.VCFO_BUILD_CHANNEL || 'dev',
  buildTime: new Date().toISOString(),
};

const out =
  `// AUTO-GENERATED by scripts/bake-defaults.js — do not edit by hand.\n` +
  `// Re-runs before every build (see package.json prebuild:electron).\n` +
  `// Override at build time with env vars:\n` +
  `//   VCFO_DEFAULT_SERVER_URL   production server base URL\n` +
  `//   VCFO_BUILD_CHANNEL        dev | staging | production\n` +
  `\n` +
  `export interface BakedDefaults {\n` +
  `  serverUrl: string;\n` +
  `  buildChannel: string;\n` +
  `  buildTime: string;\n` +
  `}\n` +
  `\n` +
  `export const BAKED: BakedDefaults = ${JSON.stringify(DEFAULTS, null, 2)};\n`;

const target = path.join(__dirname, '..', 'electron', 'baked-defaults.ts');
fs.writeFileSync(target, out, 'utf8');

console.log(`[bake-defaults] wrote ${path.relative(process.cwd(), target)}`);
console.log(`[bake-defaults]   serverUrl    = ${DEFAULTS.serverUrl}`);
console.log(`[bake-defaults]   buildChannel = ${DEFAULTS.buildChannel}`);
